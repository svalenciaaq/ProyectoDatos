
public class Colisiones {
	private Abeja first;
	private Abeja second;
	
	public Colisiones(Abeja first,Abeja second) {
		this.first=first;
		this.second=second;
	}

	public Abeja getFirst() {
		return first;
	}

	public void setFirst(Abeja first) {
		this.first = first;
	}

	public Abeja getSecond() {
		return second;
	}

	public void setSecond(Abeja second) {
		this.second = second;
	}
}
